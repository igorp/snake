class Point2 {

    private int x;
    private int y;

    public Point2() {
    }

    public Point2(int X, int Y) {
        x = X;
        y = Y;
    }

    public void setX(int X) {
        x = X;
    }

    public void setY(int Y) {
        y = Y;
    }

    public void setLocation(int X, int Y) {
        x = X;
        y = Y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
